
public class Exemplo5 {
 public static void main(String[] args) {
	 int numInteiro =10;
	 float numFrac = 50.8f;
	 
	 //Soma
	 System.out.println(numInteiro + numInteiro);
	 //Subtração
	 System.out.println(numInteiro - numInteiro);
	 //Multiplicação
	 System.out.println(numInteiro * numInteiro);
	 //Divisão
	 System.out.println(numInteiro / numInteiro);
	 
	 //Int + Double | *questão pode cair na prova
	 System.out.println(numInteiro + numFrac);
	 
	 
	 //Soma de char
	 //Resultado da soma na tabela ASCII
	 char letra1 = 'D';
	 char letra2 = 'I';
	 
	 System.out.println(letra1 + letra2);
	 
	 //String | Não é tipo de dado primitivo
	 String letraA = "A";
	 String letraB = "B";
	 System.out.println(letraA + letraB);
 }
}
